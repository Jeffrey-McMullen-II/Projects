package JavaClasses;

/**
 *
 * @author Jeffrey McMullen II
 */
public class Count 
{
    private int count = 0;

    /**
     * Return count property
     * @return 
     */
    public int getCount() 
    {
        return count;
    }

    /**
     * Increase count
     */
    public void increaseCount() 
    {
        count++;
    }
}